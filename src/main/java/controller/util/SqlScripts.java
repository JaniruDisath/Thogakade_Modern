    public static final String CREATE_CUSTOMER_TABLE = """
            CREATE TABLE IF NOT EXISTS customer (
                id VARCHAR(20) PRIMARY KEY,
                title VARCHAR(10) NOT NULL,
                name VARCHAR(100) NOT NULL,
                dob DATE NOT NULL,
                salary DOUBLE NOT NULL,
                address VARCHAR(200),
                city VARCHAR(50),
                province VARCHAR(50),
                postalCode VARCHAR(10)
            )
            """;

    public static final String CREATE_ITEM_TABLE = """
            CREATE TABLE IF NOT EXISTS item (
                itemCode VARCHAR(20) PRIMARY KEY,
                description VARCHAR(200) NOT NULL,
                packSize VARCHAR(20),
                unitPrice DOUBLE NOT NULL,
                qtyOnHand INT NOT NULL
            )
            """;

    public static final String CREATE_ORDERS_TABLE = """
            CREATE TABLE IF NOT EXISTS orders (
                orderID VARCHAR(6) NOT NULL,
                orderDate DATE,
                custID VARCHAR(6) NOT NULL,
                CONSTRAINT pk_orders PRIMARY KEY (orderID),
                CONSTRAINT fk_orders_customer FOREIGN KEY (custID) REFERENCES customer(id)
                    ON UPDATE CASCADE
                    ON DELETE CASCADE
            )
            """;

    public static final String CREATE_ORDER_DETAIL_TABLE = """
            CREATE TABLE IF NOT EXISTS orderDetail (
                orderID VARCHAR(6) NOT NULL,
                itemCode VARCHAR(6) NOT NULL,
                orderQty INT NOT NULL,
                discount INT,
                CONSTRAINT pk_orderDetail PRIMARY KEY (orderID, itemCode),
                CONSTRAINT fk_orderDetail_order FOREIGN KEY (orderID) REFERENCES orders(orderID)
                    ON UPDATE CASCADE
                    ON DELETE CASCADE,
                CONSTRAINT fk_orderDetail_item FOREIGN KEY (itemCode) REFERENCES item(itemCode)
                    ON UPDATE CASCADE
                    ON DELETE CASCADE
            )
            """;

